// arch.mjs — generate docs/ARCHITECTURE.md from accepted ADRs, and compute the
// staleness hash. The hash is over PARSED, whitespace-normalized content (accepted
// ADR choice/constraints/governs + Decision/Consequences sections + the principles
// index summary), so a whitespace-only ADR edit does NOT change it. The generated
// file is byte-deterministic under a frozen clock.

import { existsSync, readFileSync, writeFileSync, unlinkSync } from 'node:fs';
import { createHash } from 'node:crypto';
import { parse } from './frontmatter.mjs';
import { adrPath, docsPaths } from './paths.mjs';
import { statePaths } from './state.mjs';

const FROZEN_AT = '2000-01-01T00:00:00.000Z';
const GENERATOR = 'system-design/arch-sync';

const sha256 = (s) => createHash('sha256').update(s).digest('hex');
const normalize = (s) => (s ?? '').replace(/\s+/g, ' ').trim();

// Extract a `## <name>` section body from ADR markdown (until the next `## ` / EOF).
export function extractSection(body, name) {
  const re = new RegExp(`(^|\\n)##\\s+${name}\\s*\\n([\\s\\S]*?)(\\n##\\s|$)`, 'i');
  const m = re.exec(body ?? '');
  return m ? m[2] : '';
}

// All accepted ADRs across the workflow, read from their canonical markdown, sorted.
export function selectAcceptedAdrs(root, state) {
  const ids = new Set();
  for (const req of Object.values(state.requests)) {
    for (const a of req.adrs ?? []) if (a.status === 'accepted') ids.add(a.id);
  }
  const out = [];
  for (const id of [...ids].sort()) {
    const file = adrPath(root, id);
    if (!existsSync(file)) continue;
    const { data, body } = parse(readFileSync(file, 'utf8'));
    out.push({ id, data, body });
  }
  return out;
}

function principlesSummary(root) {
  const p = docsPaths(root).principlesIndex;
  if (!existsSync(p)) return { count: 0, sources: {} };
  try {
    const d = parse(readFileSync(p, 'utf8')).data;
    return { count: d.count ?? 0, sources: d.sources ?? {} };
  } catch {
    return { count: 0, sources: {} };
  }
}

// The content hash that defines freshness. Stable across whitespace-only edits.
export function archSourceHash(root, state) {
  const adrs = selectAcceptedAdrs(root, state).map((a) => ({
    id: a.id,
    title: normalize(a.data.title),
    choice: normalize(a.data.choice),
    governs: (a.data.governs ?? []).slice().sort(),
    forbids: (a.data.constraints?.forbids ?? []).map(normalize).sort(),
    requires: (a.data.constraints?.requires ?? []).map(normalize).sort(),
    decision: normalize(extractSection(a.body, 'Decision')),
    consequences: normalize(extractSection(a.body, 'Consequences')),
  }));
  return sha256(JSON.stringify({ adrs, principles: principlesSummary(root) }));
}

// Build the ARCHITECTURE.md text. `frozenClock` makes generatedAt constant (golden).
export function generateArchitecture(root, state, { frozenClock = false, clock } = {}) {
  const adrs = selectAcceptedAdrs(root, state);
  const sourceHash = archSourceHash(root, state);
  const generatedAt = frozenClock ? FROZEN_AT : clock ? clock() : new Date().toISOString();

  const fmLines = [
    '---',
    'kind: architecture',
    'generated: true',
    `generator: ${GENERATOR}`,
    `generatedAt: ${generatedAt}`,
    `sourceHash: ${sourceHash}`,
    `adrCount: ${adrs.length}`,
    'doNotEdit: true',
    '---',
  ];

  const rows = adrs.map((a) => `| \`${a.id}\` | ${normalize(a.data.title)} | ${normalize(a.data.choice)} | ${(a.data.governs ?? []).join(', ')} |`);
  const body = [
    '# Architecture',
    '',
    '> GENERATED by `engine arch-sync` from the accepted ADRs. Do not edit by hand.',
    '',
    adrs.length ? '| ADR | Title | Decision | Governs |' : '_No accepted ADRs yet._',
    ...(adrs.length ? ['|---|---|---|---|', ...rows] : []),
    '',
  ].join('\n');

  return { text: fmLines.join('\n') + '\n' + body, sourceHash, adrCount: adrs.length };
}

// Read the sourceHash recorded in the generated ARCHITECTURE.md (or null).
export function recordedHash(root) {
  const p = docsPaths(root).architecture;
  if (!existsSync(p)) return null;
  try {
    return parse(readFileSync(p, 'utf8')).data.sourceHash ?? null;
  } catch {
    return null;
  }
}

// The .architecture-stale sentinel: present => DONE is blocked until arch-sync runs.
export function markArchStale(root) {
  writeFileSync(statePaths(root).archStale, new Date(0).toISOString() + '\n');
}
export function clearArchStale(root) {
  const p = statePaths(root).archStale;
  if (existsSync(p)) unlinkSync(p);
}
export function isArchStale(root) {
  return existsSync(statePaths(root).archStale);
}
